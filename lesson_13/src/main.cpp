#include "gate.h"

int main() {
    Gate gate{100};  // Ініціалізуємо ворота відкриті на 100% (тобто закриті)

    gate.isClosed(); // Перевіряємо чи ворота відкриті чи закриті
    gate.close();    // Закриваємо ворота поступово
    gate.isClosed(); // Перевіряємо чи ворота закриті
    gate.open();     // Відкриваємо ворота поступово
    gate.isClosed(); // Перевіряємо ще раз

    return 0;
}
